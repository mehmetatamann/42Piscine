#include <stdio.h>

int	ft_atoi(char *str)
{
	int	i;
	int	c;
	int	d;

	i = 0;
	c = 1;
	d = 0;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
	{
		i++;
	}
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
		{
			c *= -1;
		}
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		d = (str[i] - '0') + (d * 10);
		i++;
	}
	return (d * c);
}
