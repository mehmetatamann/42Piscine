#include <stdio.h>
#include <unistd.h>
#include <string.h>

int	ft_str_is_printable(char *str)
{
	int	a;

	a = 0;
	while (str[a] != '\0')
	{
		if (str[a] >= 32 && str[a] <= 126)
		{
			return (1);
		}
		a++;
	}
	return (0);
}
